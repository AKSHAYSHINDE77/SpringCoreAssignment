package com.springcore.Que11;

public class Result {
	
	int rollno;
	int maths;
	int hindi;
	int english;
	int science;
	double total_marks;
	
	public Result(int rollno, int maths, int hindi, int english, int science, double total_marks) {
		super();
		this.rollno = rollno;
		this.maths = maths;
		this.hindi = hindi;
		this.english = english;
		this.science = science;
		this.total_marks = total_marks;
	}
	
	

}
